function toOpenAPI(host,h){
  const paths={};
  for(const [pat,ep] of Object.entries(h.endpoints)){
    const methods=Object.keys(ep.methods).length?Object.keys(ep.methods):["GET"];
    const pp=(pat.match(/\{[^}]+\}/g)||[]).map(m=>({name:m.slice(1,-1),in:"path",required:true,schema:{type:"string"}}));
    const qp=Object.keys(ep.params).map(k=>({name:k,in:"query",required:false,schema:{type:"string"}}));
    paths[pat]={};
    for(const m of methods) paths[pat][m.toLowerCase()]={summary:`${m} ${pat}`,parameters:[...pp,...qp],responses:{"200":{description:"OK"}}};
  }
  return {openapi:"3.0.0",info:{title:`${host} (discovered)`,version:"0.0.0",description:"Auto-generated by JS Recon. Unverified — endpoints and parameters are guesses."},servers:[{url:"https://"+host}],paths};
}
function toPostman(host,h){
  const items=[];
  for(const [pat,ep] of Object.entries(h.endpoints)){
    const methods=Object.keys(ep.methods).length?Object.keys(ep.methods):["GET"];
    const segs=pat.split("/").filter(Boolean).map(s=>s.replace(/^\{(.+)\}$/,":$1"));
    const variable=(pat.match(/\{([^}]+)\}/g)||[]).map(m=>({key:m.slice(1,-1),value:""}));
    const query=Object.keys(ep.params).map(k=>({key:k,value:""}));
    const rawQuery=query.length?"?"+query.map(q=>q.key+"=").join("&"):"";
    for(const m of methods) items.push({name:`${m} /${segs.join("/")}`,request:{method:m,header:[],url:{raw:`https://${host}/${segs.join("/")}${rawQuery}`,protocol:"https",host:host.split("."),path:segs,query,variable}}});
  }
  return {info:{name:`${host} (JS Recon)`,schema:"https://schema.getpostman.com/json/collection/v2.1.0/collection.json"},item:items};
}
function download(name,text,mime){const b=new Blob([text],{type:mime||"application/json"});const a=document.createElement("a");a.href=URL.createObjectURL(b);a.download=name;a.click();URL.revokeObjectURL(a.href);}
function flash(btn,label){const old=btn.textContent;btn.textContent=label;setTimeout(()=>btn.textContent=old,1400);}
const needHost=()=>{if(!selected){alert("Pick a host first.");return false;}return true;};

$("export").addEventListener("click",()=>{if(needHost())download(`openapi-${selected}.json`,JSON.stringify(toOpenAPI(selected,data.hosts[selected]),null,2));});
$("postman").addEventListener("click",()=>{if(needHost())download(`postman-${selected}.json`,JSON.stringify(toPostman(selected,data.hosts[selected]),null,2));});
$("copyUrls").addEventListener("click",async()=>{
  if(!needHost())return;
  const urls=[...new Set(Object.keys(data.hosts[selected].endpoints).map(p=>`https://${selected}${p}`))].sort();
  try{await navigator.clipboard.writeText(urls.join("\n"));flash($("copyUrls"),`Copied ${urls.length}`);}
  catch{download(`urls-${selected}.txt`,urls.join("\n"),"text/plain");}
});
$("exportAll").addEventListener("click",()=>download(`js-recon-${Date.now()}.json`,JSON.stringify(data,null,2)));
$("search").addEventListener("input",renderContent);
$("refresh").addEventListener("click",load);
$("theme").addEventListener("click",()=>window.__setTheme&&window.__setTheme(window.__theme()==="light"?"dark":"light"));
$("clear").addEventListener("click",async()=>{if(confirm("Clear all discovered data?")){await send({type:"CLEAR"});selected=null;load();}});

async function load(){
  const r=await send({type:"GET_DATA"}); data=r.data||{hosts:{},secrets:[]};
  if(selected&&!data.hosts[selected]) selected=null;
  updateStats(); renderHosts(); renderContent();
}
load();
setInterval(load,4000);
